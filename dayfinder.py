no_of_day=int(input("Enter number of day[1-7]: "))
if no_of_day==1:
  print("Sunday")
elif no_of_day==2:
  print("Monday")
elif no_of_day==3:
  print("Tuesday")
elif no_of_day==4:
  print("Wednesday")
elif no_of_day==5:
  print("Thursday")
elif no_of_day==6:
  print("Friday")
elif no_of_day==7:
  print("Saturday")    